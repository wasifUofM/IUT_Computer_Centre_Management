    private void Table_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_loginMouseClicked
        try {
            int row = Table_login.getSelectedRow();
            String Table_click = (Table_login.getModel().getValueAt(row, 0).toString());
            String sql = "select * from LoginInfo where UserName='" + Table_click + "'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if (rs.next()) {

                String add1 = rs.getString("UserName");
                txt_username.setText(add1);



                String add2 = rs.getString("PassWord");
                txt_password.setText(add2);

                String add3 = rs.getString("UserType");
                ComboBox_usertype.setSelectedItem(add3);
                txt_usertype.setText(add3);


            }



        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_Table_loginMouseClicked

